package fundamentos;

public class OperadoresAtribuicao {
	public static void main(String[] args) {
		int numero = 1;
		System.out.println(numero -= 1);
		System.out.println(numero += 1);
		System.out.println(numero *= 10);
		System.out.println(numero /= 10);
		System.out.println(numero %= 1);
	}
}
